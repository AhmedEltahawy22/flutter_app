// اختبار شامل لكل المسارات الممكنة - مواصلاتي
// يشغّل: flutter test scratch/test_all_routes.dart

import 'package:flutter_test/flutter_test.dart';
import 'package:flutter_app/routing_service.dart';
import 'package:flutter_app/metro_graph.dart';

void main() {
  setUpAll(() => MetroGraph.init());

  test('اختبار شامل لمسارات القاهرة', () {
    // ======================================================
    // قائمة المناطق الشهيرة في القاهرة
    // ======================================================
    final testCases = [
      // ── مترو خط 1 (حلوان ← شبرا الخيمة) ──
      {'from': 'حلوان',          'to': 'المعادي',         'note': 'خط 1 جنوب'},
      {'from': 'المعادي',        'to': 'السيدة زينب',     'note': 'خط 1 وسط'},
      {'from': 'رمسيس',          'to': 'شبرا',            'note': 'خط 1 شمال'},
      {'from': 'شبرا الخيمة',    'to': 'السادات',         'note': 'خط 1 كامل'},

      // ── مترو خط 2 (المنيب ← شبرا الخيمة) ──
      {'from': 'الجيزة',         'to': 'الدقي',           'note': 'خط 2 جيزة'},
      {'from': 'الهرم',          'to': 'وسط البلد',       'note': 'خط 2 من الهرم'},
      {'from': 'إمبابة',         'to': 'السادات',         'note': 'خط 3 شمال'},

      // ── مترو خط 3 (عدلي منصور ← الطريق الدائري) ──
      {'from': 'مدينة نصر',      'to': 'العباسية',        'note': 'خط 3 وسط'},
      {'from': 'النزهة',         'to': 'العتبة',          'note': 'خط 3 → 2'},
      {'from': 'الهايكستب',      'to': 'وسط البلد',       'note': 'خط 3 عدلي منصور'},

      // ── تبادل بين خطوط المترو ──
      {'from': 'حلوان',          'to': 'الدقي',           'note': 'خط 1→2 عبر السادات'},
      {'from': 'شبرا الخيمة',    'to': 'إمبابة',          'note': 'خط 2→3 عبر الشهداء'},
      {'from': 'المعادي',        'to': 'مدينة نصر',       'note': 'خط 1→3 عبر التبادل'},
      {'from': 'الجيزة',         'to': 'العباسية',        'note': 'خط 2→3'},
      {'from': 'حلوان',          'to': 'النزهة',          'note': 'خط 1→3 طويل'},

      // ── مسارات بمشي + مترو (bridge) ──
      {'from': 'المنيرة',        'to': 'المعادي',         'note': 'مشي+مترو خط1'},
      {'from': 'المنيرة',        'to': 'مصر القديمة',     'note': 'مشي+مترو قصير'},
      {'from': 'الدقي',          'to': 'رمسيس',           'note': 'مشي+مترو خط2→1'},
      {'from': 'السيدة زينب',    'to': 'العباسية',        'note': 'مشي+تبادل'},
      {'from': 'مصر القديمة',    'to': 'مدينة نصر',       'note': 'مشي+مترو خط1→3'},
      {'from': 'الجمالية',       'to': 'الدقي',           'note': 'مشي+مترو خط2'},
      {'from': 'القلعة',         'to': 'الجيزة',          'note': 'مشي+مترو خط1→2'},
      {'from': 'باب الشعرية',    'to': 'حلوان',           'note': 'مشي+مترو خط3→1'},

      // ── مناطق شهيرة (أتوبيس/ميكروباص) ──
      {'from': 'المنيل',         'to': 'التحرير',         'note': 'أتوبيس وسط'},
      {'from': 'عين شمس',        'to': 'المعادي',         'note': 'خط 1 طويل'},
      {'from': 'المرج',          'to': 'رمسيس',           'note': 'خط 1 المرج'},
      {'from': 'الهرم',          'to': 'الإسكندرية',      'note': 'خارج القاهرة'},

      // ── مسارات متوقعة في المناقشة ──
      {'from': 'الجامعة',        'to': 'التحرير',         'note': 'جامعة القاهرة'},
      {'from': 'مصر الجديدة',    'to': 'وسط البلد',       'note': 'مصر الجديدة'},
      {'from': 'المعادي',        'to': 'الجيزة',          'note': 'عبر السادات'},
      {'from': 'زمالك',          'to': 'مدينة نصر',       'note': 'الزمالك'},
      {'from': 'شبرا',           'to': 'الجيزة',          'note': 'شبرا للجيزة'},
    ];

    // ======================================================
    // تشغيل الاختبارات وطباعة النتائج
    // ======================================================
    int passed = 0;
    int failed = 0;
    final failedRoutes = <String>[];

    print('\n');
    print('═══════════════════════════════════════════════════════');
    print('       اختبار شامل لمسارات مواصلاتي - القاهرة');
    print('═══════════════════════════════════════════════════════');

    for (final tc in testCases) {
      final from = tc['from']!;
      final to = tc['to']!;
      final note = tc['note']!;

      final routes = RoutingService.findSmartRoutes(from, to);

      if (routes.isNotEmpty) {
        final best = routes.first;
        final modes = best.segments.map((s) => s.mode).toSet();
        final modeStr = modes
            .map((m) => m == 'metro' ? '🚇' : m == 'bus' ? '🚌' : m == 'microbus' ? '🚐' : '🚶')
            .join('+');
        print('✅ $from → $to');
        print('   ($note) | ⏱ ${best.durationMinutes} دق | 💰 ${best.price} ج.م | $modeStr');
        passed++;
      } else {
        print('❌ $from → $to ($note) - لم يُوجد مسار');
        failed++;
        failedRoutes.add('$from → $to ($note)');
      }
    }

    print('\n═══════════════════════════════════════════════════════');
    print('النتيجة: $passed نجح | $failed فشل من أصل ${testCases.length}');
    print('═══════════════════════════════════════════════════════');

    if (failedRoutes.isNotEmpty) {
      print('\nالمسارات الفاشلة:');
      for (final r in failedRoutes) {
        print('  ❌ $r');
      }
    }

    print('\n');

    // طباعة تقرير المناطق الموجودة في النظام
    print('محطات المترو المتوفرة في النظام: ${MetroGraph.stations.length} محطة');
  });
}
